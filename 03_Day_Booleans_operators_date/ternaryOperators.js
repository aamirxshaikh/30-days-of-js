let isEnabled = true;
isEnabled ? console.log("Enabled") : console.log("Disabled");

isEnabled = false;

isEnabled ? console.log("Enabled") : console.log("Disabled");

let number = 10;
number > 0
  ? console.log(`${number} is a positive number`)
  : console.log(`${number} is a negative number`);

number = -10;

number > 0
  ? console.log(`${number} is a positive number`)
  : console.log(`${number} is a negative number`);
